	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
    DCD 0x10001000 ; stack pointer value when stack is empty
    DCD Reset_Handler ; reset vector
    ALIGN
    AREA MYCODE, CODE, READONLY
    ENTRY
    EXPORT Reset_Handler
Reset_Handler
	LDR R0,=VALUE
	ADD R0,R0,#12
	LDR R1,[R0],#-04
	LDR R6,=NUM
	ADD R6,R6,#12
	LDR R2,[R6],#-04
	LDR R5,=RESULT
	ADD R5,R5,#12
	SUBS R4,R1,R2
	LDR R1,[R0],#-04
	LDR R2,[R6],#-04
	STR R4,[R5],#-04
	SBCS R4,R1,R2
	
	LDR R1,[R0],#-04
	LDR R2,[R6],#-04
	STR R4,[R5],#-04
	SBCS R4,R1,R2
	LDR R1,[R0],#-04
	LDR R2,[R6],#-04
	STR R4,[R5],#-04
	SBCS R4,R1,R2
	LDR R1,[R0],#-04
	LDR R2,[R6],#-04
	STR R4,[R5],#-04
	
VALUE DCD 0xF0101010,0xF1010101,0x10010011,0x00011011
NUM DCD 0x10101010,0xF2341234,0x10010011,0x00011011
    AREA DATA, DATA, READWRITE
RESULT DCD 0
	END
